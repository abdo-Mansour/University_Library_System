
class Person:
    def __init__(self, id, firstName, lastName, number, dob, sex, isAdmin, email= None, password= None):
        self.id = id
        self.firstName = firstName
        self.lastName = lastName
        self.number = number
        self.dob = dob
        self.sex = sex
        self.isAdmin = isAdmin
        self.email = email
        self.password = password
